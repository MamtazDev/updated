import Head from "next/head";
import Image from "next/image";
import { Inter } from "next/font/google";
import styles from "@/styles/Home.module.css";
import SearchBanner from "@/components/SearchBanner";
import Item from "@/components/Item";
import Category from "@/components/Category";
import { useEffect, useRef, useState } from "react";
import WelcomeModal from "@/utils/modals/WelcomeModal";
import { items } from "@/utils/datas/items";

export default function Home({ categories }) {
  console.log(categories)
  const welcomeModal = useRef(null);
  const [isWelcomeModal, setIsWelcomeModal] = useState(null);

  const handleCloseModal = () => {
    localStorage.removeItem("welcomeModal");
  };

  useEffect(() => {
    const data = localStorage.getItem("welcomeModal");
    if (data) {
      setIsWelcomeModal(JSON.parse(data));
      welcomeModal.current.click();
    }
  }, []);

  return (
    <>
      <Head>
        <title>Turkey Trade Market</title>
        <meta name="description" content="Generated by create next app" />
        <meta name="viewport" content="width=device-width, initial-scale=1" />
        <link rel="icon" href="favicon.ico" />
      </Head>

      <div className="home">
        <SearchBanner />
        <Category categories={categories} />
        <div className="container">
          <Item items={items} />
        </div>

        <button
          ref={welcomeModal}
          className="btn btn-primary d-none"
          data-bs-target="#exampleModalToggle1"
          data-bs-toggle="modal"
        >
          Open first modal
        </button>
      </div>

      <WelcomeModal />
    </>
  );
}

export async function getStaticProps() {
  const res = await fetch(`${process.env.REACT_APP_SERVER_URI}/categories/show/cate`);
  const categories = await res.json();
  return {
    props: {
      categories: categories.data,
    },
  };
}
